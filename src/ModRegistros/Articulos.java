    private void getArticulo(int codArticulo){
        ArticuloCtrl aCtrl = new ArticuloCtrl();
        ArticuloBean aBean = aCtrl.buscaArticuloCodigo(codArticulo);
        if(aBean != null){
            jTFCodArticulo.setText(String.valueOf(aBean.getCodArticulo()));
            jTFCodGrupo.setText(String.valueOf(aBean.getCodGrupo()));
            jTFNombreGrupo.setText(getNombreGrupo(jTFCodGrupo.getText().trim()));
            jTFCodMarca.setText(String.valueOf(aBean.getCodMarca()));
            jTFDescMarca.setText(getDescripcionMarca(jTFCodMarca.getText().trim()));
            jTFCodProveedor.setText(String.valueOf(aBean.getCodProveedor()));
            jTFRazonSocProveedor.setText(getRazonSocProveedor(jTFCodProveedor.getText().trim()));
            jTFCodSubgrupo.setText(String.valueOf(aBean.getCodSubGrupo()));
            jTFNombreSubgrupo.setText(getNombreSubGrupo(jTFCodSubgrupo.getText().trim()));
            jTFDesCorta.setText(aBean.getDesCorta());
            jTFDescripcion.setText(aBean.getDescripcion());
            jCBIva.setSelectedItem(String.valueOf(aBean.getPctIva()));
            jCBHabVenta.setSelectedItem(aBean.getHabVenta());
            jCBHabCompra.setSelectedItem(aBean.getHabCompra());
            jCBEsProduccion.setSelectedItem(aBean.getEsProduccion());
            jCBEsPesable.setSelectedItem(aBean.getEsPesable());
            jCBEsCompuesto.setSelectedItem(aBean.getEsCompuesto());
            jLNombreUsuario.setText(aBean.getCodUsuario() + " - " + getNombreUsuario(String.valueOf(aBean.getCodUsuario())));
            jLFecVigencia.setText(aBean.getFecVigencia());
        }
    }
    
