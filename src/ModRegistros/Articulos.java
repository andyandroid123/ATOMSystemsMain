    private void jTFCodSubgrupoFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jTFCodSubgrupoFocusLost
        if(jTFCodGrupo.getText().trim().equals("")){
            try{
                JOptionPane.showMessageDialog(this, "Para seleccionar un SubGrupo antes debe seleccionar un grupo!", "ATENCION!", 3);
                jTFCodGrupo.grabFocus();
            }catch(Exception ex){
                ex.printStackTrace();
                InfoErrores.errores(ex);
            }
        }else{
            if(jTFCodSubgrupo.getText().trim().equals("")){
                try{
                    jTFNombreSubgrupo.setText("ATENCION: Campo Obligatorio, debe ingresar un valor num√©rico v√°lido!");
                }catch(Exception ex){
                    ex.printStackTrace();
                    InfoErrores.errores(ex);
                }
            }else{
                try
                {
                    resultSetConsultas = DBManager.ejecutarDSL("SELECT descripcion FROM subgrupo WHERE cod_subgrupo = " + jTFCodSubgrupo.getText().trim() + " "
                        + "AND cod_grupo = " + jTFCodGrupo.getText().trim());
                    if(resultSetConsultas != null)
                    {
                        if(resultSetConsultas.next() ){
                            jTFNombreSubgrupo.setText(resultSetConsultas.getString("descripcion"));
                        }else{
                            jTFNombreSubgrupo.setText("Inexistente o no corresponde al grupo");
                        }
                    }
                }catch(Exception ex)
                {
                    ex.printStackTrace();
                    InfoErrores.errores(ex);
                }finally{
                    DBManager.CerrarStatements();
                }
            }
        }
    }//GEN-LAST:event_jTFCodSubgrupoFocusLost

