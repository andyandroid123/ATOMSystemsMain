    private void grabarBarrio(){
        try{
            String fecVigencia = "";
            try {
                SimpleDateFormat sdf = new SimpleDateFormat("dd-MM-yyyy hh:mm:ss a");
                java.util.Date d = new java.util.Date();
                fecVigencia = sdf.format(d);
            } catch (Exception ex) {
                ex.printStackTrace();
                JOptionPane.showMessageDialog(this, "Error Formateando Fecha...");
            }
            
            CiudadBean cBean = new CiudadBean();
            cBean.setCodCiudad(Integer.valueOf(jTFCodigo.getText().trim()));
            cBean.setCodUsuario(FormMain.codUsuario);
            cBean.setNombre(jTFNombreCiudad.getText().trim());
            cBean.setFecVigencia(fecVigencia);
            cBean.setCodDepartamento(Integer.valueOf(jTFCodDpto.getText().trim()));
            
            CiudadCtrl bCtrl = new CiudadCtrl();
            if(esNuevo){
                if(bCtrl.catastrarCiudad(cBean)){
                    JOptionPane.showMessageDialog(this, "Ciudad registrada con Ã©xito !!!", "OK", JOptionPane.INFORMATION_MESSAGE);
                    setComponentesGrabado();
                    cargaCiudad();
                }else{
                    JOptionPane.showMessageDialog(this, "Error al Registrar Ciudad !!!", "Error", JOptionPane.INFORMATION_MESSAGE);
                }
            }else{ // modificacion
                if(bCtrl.alterarCiudad(cBean)){
                    JOptionPane.showMessageDialog(this, "Ciudad Modificada con Suceso !!!", "OK", JOptionPane.INFORMATION_MESSAGE);
                    setComponentesGrabado();
                }else{
                    JOptionPane.showMessageDialog(this, "ATENCION: Error al Modificar Ciudad !!!", "Error", JOptionPane.WARNING_MESSAGE);
                }
            }
        }catch(Exception ex){
            ex.printStackTrace();
            InfoErrores.errores(ex);
            JOptionPane.showMessageDialog(this, "ATENCION: Error al grabar Ciudad !!!", "Error", JOptionPane.WARNING_MESSAGE);
        }
    }
    
    private void setComponentesGrabado() {

        jTFNombreCiudad.setEnabled(false);
        jTFCodDpto.setEnabled(false);

        jBNuevo.setEnabled(true);
        jBModificar.setEnabled(true);
        jBBuscarCodigo.setEnabled(true);
        jBSalir.setEnabled(true);
        jBCancelar.setEnabled(false);
        jBGuardar.setEnabled(false);
        jBNuevo.grabFocus();
    }
    
    private void cargaCiudad() {
        CiudadCtrl cCtrl = new CiudadCtrl();
        int max = cCtrl.buscaMaxCodCiudad();
        if (max > 0) {
            getCiudad(max);
        } else {
            jBModificar.setEnabled(false);
        }
    }
    
    private void getCiudad(int codigo) {
        CiudadCtrl cCtrl = new CiudadCtrl();
        CiudadBean cBean = cCtrl.buscaCiudadCodCiudad(codigo);
        if (cBean != null) {
            jTFCodigo.setText(String.valueOf(cBean.getCodCiudad()));
            jTFNombreCiudad.setText(cBean.getNombre());
            jTFCodDpto.setText(String.valueOf(cBean.getCodDepartamento()));
            jTFNombreDpto.setText(getNombreDpto(jTFCodDpto.getText().trim()));
        }
    }
    
    private String getNombreDpto(String codigo) {
        String result = "";
        if (Utiles.isIntegerValid(codigo)) {
            DptoCtrl ctrl = new DptoCtrl();
            result = ctrl.getNombreDpto(Integer.valueOf(codigo));
            ctrl = null;
        }
        return result;
    }
    
    private boolean verificaCampos(){
        if(jTFNombreCiudad.getText().trim().equals("")){
            JOptionPane.showMessageDialog(this, "ATENCION: Complete el campo NOMBRE CIUDAD !!!", "Error", JOptionPane.WARNING_MESSAGE);
            jTFNombreCiudad.requestFocus();
            return false;
        }
        return true;
    }
    
