    private void setComponentesNuevo() {
        jTFNombreCiudad.setEnabled(true);
        jTFCodDpto.setEnabled(true);

        jTFNombreCiudad.grabFocus();

        jBBuscarCodigo.setEnabled(false);
        jBCancelar.setEnabled(true);
        jBGuardar.setEnabled(true);
        jBModificar.setEnabled(false);
        jBNuevo.setEnabled(true);
        jBSalir.setEnabled(true);
    }
    
    private void setConfigCampos() {

        jTFNombreCiudad.setDocument(new MaxLength(40, "UPPER", "ALFA"));
        jTFCodDpto.setDocument(new MaxLength(5, "", "ENTERO"));
        
        jTFNombreCiudad.addFocusListener(new Focus());
        jTFCodDpto.addFocusListener(new Focus());
    }
    
    private void llenarCamposRegistro() {

        jTFCodigo.setText(getCodigoCiudad());
        jTFNombreCiudad.setText("");
        jTFCodDpto.setText("1");
    }
    
    private String getCodigoCiudad() {
        CiudadCtrl cCtrl = new CiudadCtrl();
        int result = cCtrl.buscaMaxCodCiudad();
        result = result + 1;
        return String.valueOf(result);
    }
