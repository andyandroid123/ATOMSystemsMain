    private void abmLineas(String codSubGrupo, String nombreSubGrupo, String codGrupo, String titulo, int operacion){
        Integer ZERO = new Integer(0);
        esNuevo = false;
        JTextField jTSubGrupoCod;
        JTextField jTSubGrupoNom;
        JTextField jTMargenBase ;
        JTextField jTGrupoCod;
        
        int resultDML = -1; // Para saber si el operador presiono CANCEL
        JPanel pingLinea = new JPanel(new GridLayout(8,2));
        ResultSet rset = null;
        String ultCod = null;
        
        if(operacion == 1){
            rset = DBManager.ejecutarDSL("SELECT MAX(cod_subgrupo) FROM subgrupo WHERE cod_grupo = " + jTFCodGrupo.getText().trim() + "");
            if(rset != null){
                try{
                    rset.next();
                    ultCod = rset.getString(1);
                }catch(SQLException sqlex){
                    sqlex.printStackTrace();
                    ultCod = null;
                }
            }
            if(ultCod == null){
                ultCod = codGrupo + Utiles.padLeft(3, "0", "1");
            }else{
                ultCod = String.valueOf(Integer.parseInt(ultCod) + 1);
            }
            
            jTSubGrupoCod    = new JTextField(ultCod);
            jTSubGrupoNom    = new JTextField(nombreSubGrupo);
            jTGrupoCod       = new JTextField(codGrupo);
            jTMargenBase     = new JTextField("30");
            
            
            pingLinea.add(new JLabel("Codigo de SubGrupo"));
            pingLinea.add(jTSubGrupoCod);
            jTSubGrupoCod.setEditable(false);
            pingLinea.add(new JLabel("Descripcion:"));
            pingLinea.add(jTSubGrupoNom);
            jTSubGrupoNom.setDocument(new MaxLength(40, "UPPER", "ALFA"));
            jTSubGrupoNom.requestFocus();
            pingLinea.add(new JLabel("Margen Base:"));
            pingLinea.add(jTMargenBase);
            jTMargenBase.setDocument(new MaxLength(6, "", "DOUBLE"));
            pingLinea.add(new JLabel("Codigo de Grupo"));
            pingLinea.add(jTGrupoCod);
            jTGrupoCod.setEditable(false);
        }else{
            jTSubGrupoCod    = new JTextField(codSubGrupo);
            jTSubGrupoNom    = new JTextField(nombreSubGrupo);
            jTGrupoCod       = new JTextField(codGrupo);
            jTMargenBase     = new JTextField();
            pingLinea.add(new JLabel("Codigo de SubGrupo"));
            pingLinea.add(jTSubGrupoCod);
            jTSubGrupoCod.setEditable(false);
            pingLinea.add(new JLabel("Descripcion:"));
            jTSubGrupoNom.setDocument(new MaxLength(40, "UPPER", "ALFA"));
            jTSubGrupoNom.setText(nombreSubGrupo);
            pingLinea.add(jTSubGrupoNom);
            jTSubGrupoNom.setEditable(true);
            pingLinea.add(new JLabel("Margen Base:"));
            jTMargenBase.setDocument(new MaxLength(6,"", "DOUBLE"));
            String margenBase    = String.valueOf( tGrid.getValueAt( tGrid.getSelectedRow(), 3));
            jTMargenBase.setText(margenBase);
            pingLinea.add(jTMargenBase);            
            pingLinea.add(new JLabel("Codigo de Grupo"));
            pingLinea.add(jTGrupoCod);
            jTGrupoCod.setEditable(false);
        }
        
        Integer result = new Integer(JOptionPane.showConfirmDialog(this, pingLinea, titulo, JOptionPane.OK_CANCEL_OPTION));
        try{
            if(result.equals(ZERO)){ // Presionó OK
                if(jTSubGrupoNom.getText().equals("") || jTMargenBase.getText().equals("")){
                    JOptionPane.showMessageDialog(this,"Complete los campos requeridos", "Error...", JOptionPane.WARNING_MESSAGE);
                }else{
                    switch(operacion){
                        case 1: resultDML = DBManager.ejecutarDML("INSERT INTO subgrupo (cod_subgrupo, descripcion, cod_grupo, margen_base_vta, fec_vigencia, "
                            + "cod_usuario, margen_trasferencia, es_contabilizado, desc_trasferencia) "
                            + "VALUES  (" + jTSubGrupoCod.getText() + ", '"
                                          + jTSubGrupoNom.getText() + "', "
                                          + jTGrupoCod.getText().toUpperCase() + ", "
                                          + jTMargenBase.getText() + ", '"
                                          + "now()', "
                                          + FormMain.codUsuario + ", "
                                          + "0, '"
                                          + "N', "
                                          + "0)");
                            System.out.println("SQL INSERT: " + "INSERT INTO subgrupo (cod_subgrupo, descripcion, cod_grupo, margen_base_vta, fec_vigencia, "
                                             + "cod_usuario, margen_trasferencia, es_contabilizado, desc_trasferencia) "
                                             + "VALUES (" + jTSubGrupoCod.getText() + ", '"
                                             + jTSubGrupoNom.getText() + "', "
                                             + jTGrupoCod.getText().toUpperCase() + ", "
                                             + jTMargenBase.getText() + ", '"
                                             + "now()', "
                                             + FormMain.codUsuario + ", "
                                             + "0, '"
                                             + "N', "
                                             + "0");
                        break;
                
                    case 2: 
                            resultDML = DBManager.ejecutarDML("UPDATE subgrupo SET "
                            + "descripcion = '" + jTSubGrupoNom.getText() + "', "
                            + "cod_grupo = " + jTGrupoCod.getText().toUpperCase() + ", "
                            + "margen_base_vta = 0, "
                            + "fec_vigencia = 'now()', "
                            + "cod_usuario = " + FormMain.codUsuario + ", "
                            + "margen_trasferencia = 0, "
                            + "es_contabilizado = 'N', "
                            + "desc_trasferencia = 0 "
                            + "WHERE cod_subgrupo = " + jTSubGrupoCod.getText() + " AND cod_grupo = " + jTGrupoCod.getText() + "");

                        System.out.println("SQL UPDATE: " + "UPDATE subgrupo SET "
                                                          + "descripcion = '" + jTSubGrupoCod.getText() + "', "
                                                          + "cod_grupo = '" + jTGrupoCod.getText().toUpperCase() + "', "
                                                          + "margen_base_vta = 0, "
                                                          + "fec_vigencia = 'now()', "
                                                          + "cod_usuario = " + FormMain.codUsuario + ", "
                                                          + "margen_trasferencia = 0, "
                                                          + "es_contabilizado = 'N', "
                                                          + "desc_trasferencia = 0)");
                        break;
                    default : JOptionPane.showMessageDialog(this,"Codigo de Operacion Invalido", "Error...", JOptionPane.WARNING_MESSAGE);
                    }
                }            
             }else{} // CANCEL
        }catch(Exception ex){
            ex.printStackTrace();
            InfoErrores.errores(ex);
        }
        
        
        if(resultDML != 0){
            if(resultDML == 1){
                JOptionPane.showMessageDialog(this, "Operacion Exitosa !!!");
                try{
                    DBManager.conn.commit();
                }catch(SQLException sqlex){
                    sqlex.printStackTrace();
                }
            }
        }else{
            JOptionPane.showMessageDialog(this, "ATENCION: Operacion Fallida... !!!", "Error...", JOptionPane.WARNING_MESSAGE);
        }
        DBManager.CerrarStatements();
        cargaLinea();
    }
    
    private void cargaLinea(){
        if(colLineas.isEmpty()){
            colLineas.addElement("Código");
            colLineas.addElement("Descripción");
            colLineas.addElement("Cod Grupo");
            colLineas.addElement("Margen");
        }
        String sql = "SELECT cod_subgrupo, descripcion, cod_grupo, margen_base_vta "
                   + "FROM subgrupo WHERE cod_grupo = " + jTFCodGrupo.getText().trim() + " "
                   + "ORDER BY cod_subgrupo";
        resultSetLineas = DBManager.ejecutarDSL(sql);
        tGrid.setDataSet(resultSetLineas, colLineas);
        tGrid.setVisible(true);
        tGrid.repaint();
    }
    
    private void grabarGrupo(){
        try{
            String fecVigencia = "";
            try{
                SimpleDateFormat sdf = new SimpleDateFormat("dd-MM-yyyy hh:mm:ss a");
                java.util.Date d = new java.util.Date();
                fecVigencia = sdf.format(d);
            }catch(Exception ex){
                ex.printStackTrace();
                JOptionPane.showMessageDialog(this, "Error Formatando Fecha...");
            }
            
            GrupoBean grupo = new GrupoBean();
            grupo.setCodCentroCosto(Integer.valueOf(jTFCodCentroCosto.getText().trim()));
            grupo.setCodGrupo(Integer.valueOf(jTFCodGrupo.getText().trim()));
            grupo.setCodRubro(Integer.valueOf(jTFCodRubro.getText().trim()));
            grupo.setCodUsuario(FormMain.codUsuario);
            grupo.setDescripcion(jTFNombreGrupo.getText().trim());
            grupo.setEsMateriaPrima("N");
            grupo.setFecVigencia(fecVigencia);
            grupo.setMargenBaseVenta(0);
            
            GrupoCtrl grupoCtrl = new GrupoCtrl();
            if(esNuevo){
                if(grupoCtrl.catastraGrupo(grupo)){
                    JOptionPane.showMessageDialog(this, "Grupo Registrado con Suceso !!!", "OK", JOptionPane.INFORMATION_MESSAGE);
                    setComponentesGrabado();
                    cargaGrupo();
                }else{
                    JOptionPane.showMessageDialog(this, "ATENCION: Error al Registrar Grupo !!!", "Error", JOptionPane.WARNING_MESSAGE);
                }
            }else{ //modificación
                if(grupoCtrl.modificaGrupo(grupo)){
                    JOptionPane.showMessageDialog(this, "Grupo Modificado con Suceso !!!", "OK", JOptionPane.INFORMATION_MESSAGE);
                    setComponentesGrabado();
                    cargaGrupo();
                }else{
                    JOptionPane.showMessageDialog(this, "ATENCION: Error al Modificar Grupo !!!", "Error", JOptionPane.WARNING_MESSAGE);
                }                    
            }
        }catch(Exception ex){
            ex.printStackTrace();
            InfoErrores.errores(ex);
            JOptionPane.showMessageDialog(this, "ATENCION: Error al grabar Grupo !!!", "Error", JOptionPane.WARNING_MESSAGE);
        }
    }
    
