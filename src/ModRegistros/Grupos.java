    private String obtenerCodigoGrupo(){        
        GrupoCtrl grupoCtrl = new GrupoCtrl();
        int result = grupoCtrl.buscaMaxCodGrupo();
        result = result + 1;
        return String.valueOf(result);
    }
    
    private void cargaGrupo(){
        GrupoCtrl grupo = new GrupoCtrl();
        int max = grupo.buscaMaxCodGrupo();
        if(max > 0){
            getGrupo(max);
        }else{
            jBModificar.setEnabled(false);
        }
    }
    
    private void cargaGrupoCall(String codigo){
        
        getGrupo(Integer.valueOf(codigo));
    }
    
    private void getGrupo(int codGrupo){
        GrupoCtrl grupoCtrl = new GrupoCtrl();
        GrupoBean grupoBean = grupoCtrl.buscaGrupoCodigo(codGrupo);
        if(grupoBean != null){
            jTFCodGrupo.setText(String.valueOf(grupoBean.getCodGrupo()));
            jTFNombreGrupo.setText(grupoBean.getDescripcion());
            
            jTFCodCentroCosto.setText(String.valueOf(grupoBean.getCodCentroCosto()));
            jLDescripcionCentroCosto.setText(getDescripcionCentroCosto(jTFCodCentroCosto.getText().trim()));
            
            jTFCodRubro.setText(String.valueOf(grupoBean.getCodRubro()));
            jLDescripcionRubro.setText(getDescripcionRubro(jTFCodRubro.getText().trim()));
            
            jTFFecVigencia.setText(grupoBean.getFecVigencia());
            jTFNombreUsuario.setText(grupoBean.getCodUsuario() + "  -  " + getNombreUsuario(String.valueOf(grupoBean.getCodUsuario())));
        }
    }
    
    private String getNombreUsuario(String codGrupo){
        String result = "";
        if(Utiles.isIntegerValid(codGrupo)){
            UsuarioCtrl usuarioCtrl = new UsuarioCtrl();
            result = usuarioCtrl.getNombreUsuario(Integer.valueOf(codGrupo));
            usuarioCtrl = null;
        }
        return result;
    }
    
    private String getDescripcionRubro(String codigo) {
        String result = "";
        if (Utiles.isIntegerValid(codigo)) {
            RubroCtrl ctrl = new RubroCtrl();
            result = ctrl.getDescripcionGrupo(Integer.valueOf(codigo));
            ctrl = null;
        }
        return result;
    }
    
    private String getDescripcionCentroCosto(String codigo) {
        String result = "";
        if (Utiles.isIntegerValid(codigo)) {
            CentroCostoCtrl ctrl = new CentroCostoCtrl();
            result = ctrl.getNombreCentroCosto(Integer.valueOf(codigo));
            ctrl = null;
        }
        return result;
    }
    
