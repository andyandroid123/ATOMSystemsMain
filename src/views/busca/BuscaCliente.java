    protected void cargaProveedoresGrid(List<ClienteBean> clienteBean){
        while (dtmClientes.getRowCount() > 0){
            dtmClientes.removeRow(0);
        }
        
        if (clienteBean.size() == 0){
            JOptionPane.showMessageDialog(this, "ATENCION: Ningún Cliente Encontrado !!!", "Busca", JOptionPane.INFORMATION_MESSAGE);
        }else{
            String [] campos = new String[] {null, null, null, null};
            for (int i = 0; i < clienteBean.size(); i++){
                dtmClientes.addRow(campos);
                dtmClientes.setValueAt(clienteBean.get(i).getCodCliente(), i, 0);
                dtmClientes.setValueAt(clienteBean.get(i).getRazonSoc(), i, 1);
                dtmClientes.setValueAt(clienteBean.get(i).getRucCliente(), i, 2);
                dtmClientes.setValueAt(clienteBean.get(i).getTelefono(), i, 3);
            }
        }
    }

