    protected void jtCiudadesLinhaSelecionada(JTable tb) {
        if (tb.getSelectedRow() != -1) {
            jbSelecciona.setEnabled(true);
            jbSelecciona.setSelected(true);
        } else {
            jbSelecciona.setEnabled(false);
            jbSelecciona.setSelected(false);
        }
    }

