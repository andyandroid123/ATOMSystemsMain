private void jbBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbBuscarActionPerformed
    ClienteCtrl clienteCtrl = new ClienteCtrl();
    clienteBeanList.clear();
    if (jrbNombre.isSelected()) {
        clienteBeanList = clienteCtrl.listarClienteRazonSoc("%" + jtfInputBusca.getText().trim() + "%");
    } else if(jrbRuc.isSelected()){
        clienteBeanList = clienteCtrl.listarClienteRuc("%" + jtfInputBusca.getText().trim() + "%");
    }else {
        if (Utiles.isIntegerValid(jtfInputBusca.getText().trim())) {
            clienteBeanList.add(clienteCtrl.buscaClienteCodCliente(Integer.valueOf(jtfInputBusca.getText().trim())));
        }
    }

    if (clienteBeanList.size() > 0) {
        cargaProveedoresGrid(clienteBeanList);
        jTClientes.grabFocus();
    }
}//GEN-LAST:event_jbBuscarActionPerformed

