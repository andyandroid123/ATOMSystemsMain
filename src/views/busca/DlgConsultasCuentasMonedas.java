    private void llenarTabla(String texto){
        try{
            limpiarGrillas();
            rs = DBManager.ejecutarDSL("SELECT cod_cuenta, denominacion_cta, moneda.cotiz_compra, cuenta.activa "
                                     + "FROM cuenta "
                                     + "LEFT OUTER JOIN moneda "
                                     + "ON cuenta.cod_moneda = moneda.cod_moneda "
                                     + "WHERE denominacion_cta LIKE '%" + texto + "%' ORDER BY cod_cuenta");
            if(rs != null){
                while(rs.next()){
                    Object[] row = new Object[4];
                    row[0] = rs.getInt("cod_cuenta");
                    row[1] = rs.getString("denominacion_cta");
                    row[2] = rs.getFloat("cotiz_compra");
                    row[3] = rs.getString("activa");
                    dtm.addRow(row);
                }
                jTDetallesCuenta.updateUI();
                jTDetallesCuenta.grabFocus();
            }
        }catch(Exception ex){
            ex.printStackTrace();
            InfoErrores.errores(ex);
        }
    }
    
