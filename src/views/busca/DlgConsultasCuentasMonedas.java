    private void jTFDescripcionKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTFDescripcionKeyPressed
        if(evt.getKeyCode() == KeyEvent.VK_ENTER){
            if(!jTFDescripcion.getText().trim().equals("")){
                llenarTabla(jTFDescripcion.getText().trim());
                if(jTDetallesCuenta.getRowCount() > 0)
                {
                    jTDetallesCuenta.setRowSelectionInterval(0, 0);
                    jTDetallesCuenta.grabFocus();
                }
            }
        }
    }//GEN-LAST:event_jTFDescripcionKeyPressed

    private void jTDetallesCuentaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTDetallesCuentaKeyPressed
        if(evt.getKeyCode() == evt.VK_ENTER)
        {
            jText.setText(String.valueOf(jTDetallesCuenta.getValueAt(jTDetallesCuenta.getSelectedRow(), 0)));
            dispose();
        }
        if(evt.getKeyCode() == evt.VK_ESCAPE)
        {
            jText.setText(jText.getText());
            dispose();
        }
    }//GEN-LAST:event_jTDetallesCuentaKeyPressed

