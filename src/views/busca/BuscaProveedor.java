    protected void cargaProveedoresGrid(List<ProveedorBean> proveedorBean){
        while (dtmProveedores.getRowCount() > 0){
            dtmProveedores.removeRow(0);
        }
        
        if (proveedorBean.size() == 0){
            JOptionPane.showMessageDialog(this, "ATENCION: Ningún Proveedor Encontrado !!!", "Busca", JOptionPane.INFORMATION_MESSAGE);
        }else{
            String [] campos = new String[] {null, null, null, null};
            for (int i = 0; i < proveedorBean.size(); i++){
                dtmProveedores.addRow(campos);
                dtmProveedores.setValueAt(proveedorBean.get(i).getCodProveedor(), i, 0);
                dtmProveedores.setValueAt(proveedorBean.get(i).getRazonSocial(), i, 1);
                dtmProveedores.setValueAt(proveedorBean.get(i).getRucProveedor(), i, 2);
                dtmProveedores.setValueAt(proveedorBean.get(i).getTelefono(), i, 3);
            }
        }
    }

