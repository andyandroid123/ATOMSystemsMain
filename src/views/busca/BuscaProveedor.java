private void jbBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbBuscarActionPerformed
    ProveedorCtrl proveedorCtrl = new ProveedorCtrl();
    proveedorBeanList.clear();
    if (jrbNombre.isSelected()) {
        proveedorBeanList = proveedorCtrl.listarProveedorRazonSoc("%" + jtfInputBusca.getText().trim() + "%");
    } else if(jrbRuc.isSelected()){
        proveedorBeanList = proveedorCtrl.listarProveedorRuc("%" + jtfInputBusca.getText().trim() + "%");
    }else {
        if (Utiles.isIntegerValid(jtfInputBusca.getText().trim())) {
            proveedorBeanList.add(proveedorCtrl.buscaProveedorCodigo(Integer.valueOf(jtfInputBusca.getText().trim())));
        }
    }

    if (proveedorBeanList.size() > 0) {
        cargaProveedoresGrid(proveedorBeanList);
        jTGrupos.grabFocus();
    }
}//GEN-LAST:event_jbBuscarActionPerformed

