    private void registroSiguiente(){
        DBManager.CerrarStatements();
        StatementManager sm = new StatementManager();
        sm.TheSql = "SELECT min(cod_empleado) FROM empleado WHERE cod_empleado > " + jTFCodEmpleado.getText();
        sm.EjecutarSql();
        try {
            if (sm.TheResultSet.next()) {
                if (sm.TheResultSet.getString(1) != null) //Long.valueOf(jTCodArticulo.getText())) 
                {
                    getEmpleado(Integer.parseInt(sm.TheResultSet.getString(1)));
                    abrirImagenEmpleado(jTFCodEmpleado.getText());
                } else {
                    jBAnterior.grabFocus();
                    JOptionPane.showMessageDialog(this, "Ya no existen registros para mostrar!", "OK", JOptionPane.INFORMATION_MESSAGE);
                }
            }
        } catch (SQLException sex) {
            sex.printStackTrace();
            InfoErrores.errores(sex);
        } finally {
            sm.CerrarStatement();  // implicito cierre de resultset
            sm.ClearDBManagerstmt();
            sm = null;
            System.gc();
        }
    }
    
