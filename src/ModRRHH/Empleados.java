    private String obtenerCodigoEmpleado(){        
        EmpleadoCtrl eCtrl = new EmpleadoCtrl();
        int result = eCtrl.buscaMaxCodEmpleado();
        result = result + 1;
        return String.valueOf(result);
    }
    
    private void getFecVigencia(){
        try {
                //SimpleDateFormat sdf = new SimpleDateFormat("dd-MM-yyyy hh:mm:ss a");
                SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
                java.util.Date d = new java.util.Date();
                fecVigencia = sdf.format(d);
            } catch (Exception ex) {
                ex.printStackTrace();
                JOptionPane.showMessageDialog(this, "Error Formateando Fecha...");
            }
    }
    
    private void cargaEmpleado() {
        EmpleadoCtrl pCtrl = new EmpleadoCtrl();
        int max = pCtrl.buscaMaxCodEmpleado();
        if (max > 0) {
            getEmpleado(max);
        } else {
            jBModificar.setEnabled(false);
        }
    }
    
    private void getEmpleado(int codigo){
        EmpleadoCtrl ctrl = new EmpleadoCtrl();
        EmpleadoBean bean = ctrl.buscaEmpleadoCodigo(codigo);
        if(bean != null){
            jTFCodEmpleado.setText(String.valueOf(bean.getCodEmpleado()));
            jTFCI.setText(String.valueOf(bean.getCipEmpleado()));
            jTFCodLocal.setText(String.valueOf(bean.getCodLocal()));
            jLDescLocal.setText(getDescripcionLocal(jTFCodLocal.getText()));
            jTFNombre.setText(bean.getNombre());
            jTFApellido.setText(bean.getApellido());
            jTFDireccion.setText(bean.getDireccion());
            jTFCodBarrio.setText(String.valueOf(bean.getCodBarrio()));
            jLNombreBarrio.setText(getNombreBarrio(jTFCodBarrio.getText().trim()));
            jTFCodCiudad.setText(String.valueOf(bean.getCodCiudad()));
            jLNombreCiudad.setText(getNombreCiudad(jTFCodCiudad.getText()));
            jTFCodProfesion.setText(String.valueOf(bean.getCodProfesion()));
            jLDescProfesion.setText(getDescProfesion(jTFCodProfesion.getText()));
            jTFCodUsuarioDelSistema.setText(String.valueOf(bean.getCodUsuarioSistema()));
            jLNombreUsuarioSistema.setText(getNombreUsuario(jTFCodUsuarioDelSistema.getText()));
            jLPathImagen.setText(bean.getPathImg());
            String activo = bean.getActivo();
            if(activo.equals("S")){
                jRBActivo.setSelected(true);
            }else{
                jRBInactivo.setSelected(true);
            }
            jTFTelFijo.setText(bean.getTelefonoFijo());
            jTFTelMovil.setText(bean.getTelefonoMovil());
            jTFEmail.setText(bean.getEmail());
            jTFCodNacionalidad.setText(String.valueOf(bean.getCodNacionalidad()));
            jLNacionalidad.setText(getGentilicio(jTFCodNacionalidad.getText()));
            jTFFecNacimiento.setText(bean.getFecNacimiento());
            jTFLugarNacimiento.setText(bean.getLugarNacimiento());
            jCBECivil.setSelectedItem(String.valueOf(bean.getEstadoCivil()));
            labelEstadoCivil();
            jCBSexo.setSelectedItem(String.valueOf(bean.getSexo()));
            labelSexo();
            jTFCantHijo.setText(String.valueOf(bean.getCanHijos()));
            jTFNombreConyugue.setText(bean.getNombreConyugue());
            jTFCodSec.setText(String.valueOf(bean.getCodSeccion()));
            jLDescSec.setText(getDescSeccion(jTFCodSec.getText()));
            jTFCodCargo.setText(String.valueOf(bean.getCodCargo()));
            jLDescCargo.setText(getDescCargo(jTFCodCargo.getText()));
            jTFCodHorario.setText(String.valueOf(bean.getCodHorario()));
            jLDescHorario.setText(getDescHorario(jTFCodHorario.getText()));
            jTFFecIngreso.setText(bean.getFecIngreso());
            jTFFecEgreso.setText(bean.getFecEgreso());
            jTFMotSalida.setText(bean.getMotivoSalida());
            jTFFecIngIps.setText(bean.getFecIngresoIps());
            jTFNroAsegIps.setText(bean.getNroAsegIps());
            String descuentaIps = bean.getDescuentaIps();
            if(descuentaIps.equals("S")){
                jChBDescuentaIps.setSelected(true);
            }else{
                jChBDescuentaIps.setSelected(false);
            }
            String figuraPlanilla = bean.getFiguraPlanilla();
            if(figuraPlanilla.equals("S")){
                jChBFiguraPlanilla.setSelected(true);
            }else{
                jChBFiguraPlanilla.setSelected(false);
            }
            String permiteMarcacion = bean.getPermiteMarcacion();
            if(permiteMarcacion.equals("S")){
                jChBPermiteMarcacion.setSelected(true);
            }else{
                jChBPermiteMarcacion.setSelected(false);
            }
            jTAObs.setText(bean.getRecomendacion());
            String nivelAcademico = bean.getNivelAcademico();
            if(nivelAcademico.equals("P")){
                jRBPrimario.setSelected(true);
            }
            if(nivelAcademico.equals("S")){
                jRBSecundario.setSelected(true);
            }
            if(nivelAcademico.equals("U")){
                jRBUniversitario.setSelected(true);
            }
            jTAObsNivelAcademico.setText(bean.getObsNivelAcademico());
            jTFSueldo1.setText(String.valueOf(bean.getSueldo1()));
            jTFSueldo2.setText(String.valueOf(bean.getSueldo2()));
            jTFComision.setText(String.valueOf(bean.getPctComision()));
            jCBHorasExtras.setSelectedItem(bean.getHoraExtra());
            jTFHorasExtras.setText(String.valueOf(bean.getHorasExtrasAut()));
            String codUsuario = String.valueOf(bean.getCodUsuario());
            jTFUsuario.setText(String.valueOf(bean.getCodUsuario()) + " - " + getNombreUsuario(codUsuario));
            jTFFecVigencia.setText(bean.getFecVigencia());
        }
    }
    
