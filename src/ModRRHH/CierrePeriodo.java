    private void jBProcesarLiquidacionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBProcesarLiquidacionActionPerformed
        Object[] options = {"Si", "No"};
        int inputValue = JOptionPane.showOptionDialog(null, "Realmente desea procesar la liquidación definitiva?", "Atención",
                JOptionPane.DEFAULT_OPTION, JOptionPane.QUESTION_MESSAGE, null, options, options[1]);
        if(inputValue == 0){
            boolean isOk = true;
            if(!utiles.Utiles.esFechaValida(jTFDesde.getText())){
                JOptionPane.showMessageDialog(this, "ATENCION: Fecha NO VALIDA !!!", "AtenciÓn", JOptionPane.WARNING_MESSAGE);
                jTFDesde.grabFocus();
                isOk = false;
            }
            if(!utiles.Utiles.esFechaValida(jTFHasta.getText())){
                JOptionPane.showMessageDialog(this, "ATENCION: Fecha NO VALIDA !!!", "AtenciÓn", JOptionPane.WARNING_MESSAGE);
                jTFHasta.grabFocus();
                isOk = false;
            }
            if(isOk){
                boolean grabaOk = false;
                String sql = "UPDATE periodo_rrhh SET "
                           + "vigente = 'N', "
                           + "cerrado = 1, "
                           + "cod_usuario = " + FormMain.codUsuario + ", "
                           + "fec_vigencia = 'now()' "
                           + "WHERE vigente = 'S' "
                           + "AND cerrado = 0 "
                           + "AND cod_empresa = " + codEmpresa + " "
                           + "AND cod_local = " + codLocal;
                System.out.println("UPDATE PERIODO RRHH: " + sql);
                if(DBManager.ejecutarDML(sql) > 0){
                    try{
                        int idPeriodo = newIdPeriodo();
                        sql = "INSERT INTO periodo_rrhh(idperiodo, periodo, diaslaborales, fechainicial, fechafinal, vigente, cerrado, cod_empresa, "
                            + "cod_local, fec_vigencia, cod_usuario, sueldo_minimo, fec_tope_anticipo) "
                            + "VALUES ( " + idPeriodo + ", '" + jTFSgtePeriodo.getText().replace("/", "")+ "', " + jTFDiasLaborales.getText() + ", "
                            + "TO_DATE('" + jTFDesde.getText() + "', 'dd/MM/yyyy'), TO_DATE('" + jTFHasta.getText() + "', 'dd/MM/yyyy'), 'S', 0, " + codEmpresa + ", "
                            + "" + codLocal + ", 'now()', " + FormMain.codUsuario + ", " + getSalarioMinActual() + ", 'now()')";
                        System.out.println("INSERT NUEVO PERIODO RRHH: " + sql);
                        if(DBManager.ejecutarDML(sql) > 0){
                            try{
                                grabaOk = true;
                                DBManager.conn.commit();
                                JOptionPane.showMessageDialog(this, "Proceso finalizado con éxito!", "Suceso...", JOptionPane.INFORMATION_MESSAGE);
                                this.dispose();
                            }catch(Exception ex){
                                ex.printStackTrace();
                                grabaOk = false;
                            }
                        }else{
                            JOptionPane.showMessageDialog(this, "Error al insertar nuevo periodo, Operación Cancelada!", "Error", JOptionPane.WARNING_MESSAGE);
                        }
                    }catch(Exception ex){
                        ex.printStackTrace();
                        InfoErrores.errores(ex);
                    }
                }else{
                    JOptionPane.showMessageDialog(this, "Error al Actualizar Datos, Operación Cancelada!", "Error", JOptionPane.WARNING_MESSAGE);
                }
                if(!grabaOk){
                    try{
                        DBManager.conn.rollback();
                    }catch(Exception ex){
                        ex.printStackTrace();
                    }
                }
            }
        }else{
            this.dispose();
        }
    }//GEN-LAST:event_jBProcesarLiquidacionActionPerformed

    private int newIdPeriodo(){
        int result = 0;
        try{
            ResultSet rs = DBManager.ejecutarDSL("SELECT MAX(idperiodo) + 1 idPeriodo "
                                               + "FROM periodo_rrhh "
                                               + "WHERE cod_empresa = " + codEmpresa + " "
                                               + "AND cod_local = " + codLocal);
            if(rs != null){
                if(rs.next()){
                    result = rs.getInt("idPeriodo");
                }
            }
        }catch(Exception ex){
            ex.printStackTrace();
            InfoErrores.errores(ex);
        }
        return result;
    }
    
    private void setDatosInicio(){
        String perACerrar = utiles.Utiles.padLeft(6, "0", utiles.Utiles.getPeriodoRRHH(codEmpresa, codLocal));
        String sgtePeriodo = utiles.Utiles.padLeft(6, "0", utiles.Utiles.getSiguientePeriodoRRHH(perACerrar));
        jTFPeriodo.setText(perACerrar.substring(0,2) + "/" + perACerrar.substring(2,6));
        jTFSgtePeriodo.setText(sgtePeriodo.substring(0,2) + "/" + sgtePeriodo.substring(2,6));
        String mes = sgtePeriodo.substring(0,2);
        jTFDesde.setText("01/" + jTFSgtePeriodo.getText());
        jTFHasta.setText(diasDelMes(Integer.parseInt(mes))+ "/" + jTFSgtePeriodo.getText());
        jTFDiasLaborales.setText(getDiasLaboralesFiscal());
        jTFSalarioMinimo.setText(decimalFormat.format(Integer.parseInt(getSalarioMinActual())));
    }
    
    private String getSalarioMinActual(){
        String result = "";
        try{
            String sql = "SELECT valor FROM parametro_rrhh WHERE parametro = 'RRHH_SALARIO_MINIMO_ACTUAL'";
            ResultSet rs = DBManager.ejecutarDSL(sql);
            if(rs != null){
                if(rs.next()){
                    result = rs.getString("valor");
                }
            }
        }catch(Exception ex){
            ex.printStackTrace();
            InfoErrores.errores(ex);
        }finally{
            DBManager.CerrarStatements();
        }
        return result;
    }
    
    private String getDiasLaboralesFiscal(){
        String result = "";
        try{
            String sql = "SELECT valor FROM parametro_rrhh WHERE parametro = 'RRHH_DIAS_FISCAL'";
            ResultSet rs = DBManager.ejecutarDSL(sql);
            if(rs != null){
                if(rs.next()){
                    result = rs.getString("valor");
                }
            }
        }catch(Exception ex){
            ex.printStackTrace();
            InfoErrores.errores(ex);
        }finally{
            DBManager.CerrarStatements();
        }
        return result;
    }
    
    private int diasDelMes(int mes){
        int result = 0;
        switch(mes){
            case 1: case 3: case 5: case 7: case 8: case 10: case 12:
                result = 31;
                break;
            case 4: case 6: case 9: case 11:
                result = 30;
                break;
            case 2:
                result = 28;
                break;
            default:
                result = 0;
        }
        return result;
    }
